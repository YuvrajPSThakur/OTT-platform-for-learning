class Progress {
  String heading  ;
  String tutor ;
  String weeksleft ;
  int points ;
  String imageL ;
  String totallectures ;
  String cost ;
  Progress ( {String heading , String tutor ,
    String weeksleft , int points , String imageL , String totallectures , String cost}){
    this.heading = heading ;
    this.tutor = tutor;
    this.weeksleft = weeksleft;
    this.points = points ;
    this.imageL = imageL ;
    this.totallectures = totallectures ;
    this.cost = cost ;

  }
}
